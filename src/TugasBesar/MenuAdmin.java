    private void tahunEditKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tahunEditKeyPressed
        // TODO add your handling code here:
        index = -1;
        int key = evt.getKeyCode();
        if (key == 10){
            String NIP = NIPPerizinanFieldEdit.getText();
            String noTanggal = tanggalEdit.getText();
            String noBulan = bulanEdit.getText();
            String noTahun = tahunEdit.getText();
            String tanggal = noTanggal +"-"+ noBulan +"-"+ noTahun;
            if (NIP.trim().isEmpty()){
                JOptionPane.showMessageDialog(null, "Silahkan isi NIP terlebih dahulu!");
            } else {
                for (int i = 0; i< perizinans.size(); i++){
                    Perizinan perizinan = perizinans.get(i);
                    if(NIP.equalsIgnoreCase(perizinan.getNIP()) && tanggal.equals(perizinan.getTanggal())){
                        index=i;
                        break;
                    } else {
                        index = -1;
                    }
                }

                if (index >= 0){
                    for (Perizinan perizinan : perizinans) {
                        if (NIP.equalsIgnoreCase(perizinan.getNIP()) && tanggal.equals(perizinan.getTanggal())){
                            //show content
                            namaPerizinanEdit.show();
                            jabatanPerizinanEdit.show();
                            alasanPerizinanEdit.show();
                            lamaPerizinanEdit.show();
                            jenisPerizinanEdit.show();
                            noSuratPerizinanEdit.show();
                            namaContentPerizinanEdit.show();
                            jabatanContentPerizinanEdit.show();
                            radioIzinEdit.show();
                            radioCutiEdit.show();
                            radioSakitEdit.show();
                            alasanPerizinanFieldEdit.show();
                            lamaPerizinanFieldEdit.show();
                            noSuratPerizinanFieldEdit.show();
                            buttonSaveEditPerizinan.show();
                            saveEditPerizinan.show();
                            
                            //set text
                            namaContentPerizinanEdit.setText(perizinan.getNama());
                            jabatanContentPerizinanEdit.setText(perizinan.getJabatan());
                            alasanPerizinanFieldEdit.setText(perizinan.getAlasan());
                            lamaPerizinanFieldEdit.setText(perizinan.getLamaWaktu());
                            if (perizinan.getJenisIzin().equalsIgnoreCase("Izin")){
                                radioIzinEdit.setSelected(true);
                            } else if (perizinan.getJenisIzin().equalsIgnoreCase("Cuti")){
                                radioCutiEdit.setSelected(true);
                            } else if (perizinan.getJenisIzin().equalsIgnoreCase("Sakit")){
                                radioSakitEdit.setSelected(true);
                            }
                            noSuratPerizinanFieldEdit.setText(perizinan.getNoSurat());
                            alasanPerizinanEdit.requestFocus();
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                }
            }
        }
    }//GEN-LAST:event_tahunEditKeyPressed

